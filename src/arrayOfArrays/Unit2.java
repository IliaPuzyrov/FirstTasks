package arrayOfArrays;

public class Unit2 {
    public static void main(String[] args) {
        for (int a = 1; a <= 10; a++) {
            for (int i = 1; i <= 10; i++) {
                int e = a * i;
                System.out.print(e + "|" + "\t");
            }
            System.out.println("");

        }


    }


}
